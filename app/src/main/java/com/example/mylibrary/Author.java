package com.example.mylibrary;

public class Author {


    int id;
    String name;

    public void setid(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }


    public int getId() {
        return this.id;
    }

    public String getName() {
        return this.name;
    }



}
